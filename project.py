def find_zone(dx, dy):
      if abs(dx) <= abs(dy):
          if dx >= 0 and dy >= 0:
              return 1
          elif dx <= 0 and dy >= 0:
              return 2
          elif dx >= 0 and dy <= 0:
              return 6
          elif dx <= 0 and dy <= 0:
              return 5
      else:
          if dx >= 0 and dy >= 0:
              return 0
          elif dx <= 0 and dy >= 0:
              return 3
          elif dx >= 0 and dy <= 0:
              return 7
          elif dx <= 0 and dy <= 0:
              return 4
 
def ozone(z, x, y):
      match z:
          case 0:
              return x, y
          case 1:
              return y, x
          case 2:
              return -y, x
          case 3:
              return -x, y
          case 4:
              return -x, -y
          case 5:
              return -y, -x
          case 6:
              return y, -x
          case 7:
              return x, -y
 
 
def zone0(z, x, y):
      match z:
          case 0:
              return x, y
          case 1:
              return y, x
          case 2:
              return y, -x
          case 3:
              return -x, y
          case 4:
              return -x, -y
          case 5:
              return -y, -x
          case 6:
              return -y, x
          case 7:
              return x, -y
 
 
def lining(x, y):
    glPointSize(10)
 
    glBegin(GL_POINTS)
    glColor3f(0, 0, 0)
    glVertex2f(x, y)
    glEnd()
 
def mpl (x0, y0, x1, y1, z):
      dx = x1 - x0
      dy = y1 - y0
 
      d = (2 * dy) - dx
      e = 2 * dy
      ne = 2 * (dy - dx)
 
      x = x0
      y = y0
 
      while x < x1:
          px, py = ozone(z, x, y)
          lining(px, py)
          if d < 0:
              x += 1
              d += e
          else:
              x += 1
              y += 1
              d += ne
 
def lines (x0, y0, x1, y1):
      dx = x1 - x0
      dy = y1 - y0
      zone = find_zone(dx, dy)
      px0, py0 = zone0(zone, x0, y0)
      px1, py1 = zone0(zone, x1, y1)
 
      mpl( px0, py0, px1, py1, zone)
 
 
         
